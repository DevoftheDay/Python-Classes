import turtle
turtle.Screen().bgcolor("orange")
turtle.Screen().setup(300,400)
triangle = turtle.Turtle()

triangle.forward(100)
triangle.right(120)
triangle.forward(100)
triangle.right(120)
triangle.forward(100)
triangle.penup()
triangle.right(120)
triangle.forward(50)
triangle.left(90)
triangle.forward(30)
triangle.left(150)

triangle.pendown()
triangle.forward(100)
triangle.left(120)
triangle.forward(100)
triangle.left(120)
triangle.forward(100)
turtle.done()